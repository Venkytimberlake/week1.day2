package week1.day2;

public class Calculator {
	
	public double addnum(int a1, int a2, int a3) {
	double sum=a1+a2+a3;
	return sum;
}
	public double subnum(int a1, int a2) {
		double sub=a1-a2;
		return sub;
	}
	public double mulnum(int a1, int a2) {
		double mul=a1*a2;
		return mul;
	}
	public double divnum(int a1, int a2) {
		double div=a1/a2;
		return div;
	}
}